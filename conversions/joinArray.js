let array = ['one', 'two', 'three'];
console.log('Before the transformation, the array is:', array);

// the join function does not return the result, like parseInt() and toString()
// so you have to do everything in one line.
console.log('The string with the array combined, connected with |:', array.join('|'));
console.log();
console.log('Notice how the join() function turns an array into a string');
