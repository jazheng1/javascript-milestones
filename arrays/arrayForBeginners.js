let coolArray = [1, 2, 3, 4, 5];
console.log('The array is:', coolArray);
// to get the length of an array you can do (name of array).length
console.log('The length of the array is:', coolArray.length);
console.log();

// to get an element in an array you can do (name of array)[the number where the element is located]
console.log('The first element of the array is:', coolArray[0]);
console.log('The second element of the array:', coolArray[1]);
// because arrays start at zero, to get the last element of an array you can always do the length of the array -1;
console.log('The last element of the array is:', coolArray[coolArray.length-1]);
